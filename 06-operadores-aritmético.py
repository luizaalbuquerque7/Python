# Operadores aritmético
# Soma
soma = 10 + 5
print(soma)
# Subtração
subtração = 10 - 5
print(subtração)
# Multiplicação
multiplicação = 10 * 5
print(multiplicação)
# Divivsão
divisao = 10 / 5
print(divisao)
# Divisão inteira
divisao_inteira = 10 // 5
print(divisao_inteira)
#Exponenciação
exponenciacao = 10 ** 2
print(exponenciacao)
# Módulo / Resto da Divisão
modulo = 10 % 5 
print(modulo)